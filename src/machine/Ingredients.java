package machine;

public class Ingredients {

    public final int WATER;
    public final int MILK;
    public final int COFFEE_BEANS;
    public final int CUPS;
    public final int PRICE;

    public Ingredients(int water, int milk, int coffeeBeans, int cups, int money) {
        this.WATER = water;
        this.MILK = milk;
        this.COFFEE_BEANS = coffeeBeans;
        this.CUPS = cups;
        this.PRICE = money;
    }
}